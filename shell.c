void    prompt()
{
   
    ft_putstr_fd("\033[0;36mMy_Minishell $>\e[0m", 0);
}
// int ac ,char **av, char **env
int     main()
{
    t_token_list *var;
    char *line;
    int r;

    var = NULL;
    line = NULL;
    banner();
    while (1)
    {
        prompt();
        r = get_next_line(0, &line);
        if (r > 0)
        {
           // ft_putstr_fd(line, 0);
		    write(1, "\n",1);
        }
        var = ft_lexer(line);
        display_token(var);
        check_syntax_error(var);

        if (ft_strcmp((const char*)line,"exit") == 0)
        {
            free(line);
            break;
        }
    }
    return 0;
}
